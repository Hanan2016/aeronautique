package aeronautique;

public class Pilote {
	
 private int numeroPilote;
 private String nomPilote;
 private String adressePilote;
 private int salairePilote;

 
 protected Pilote(int numeroPilote, String nomPilote, String adressePilote, int salairePilote) {
	super();
	this.numeroPilote = numeroPilote;
	this.nomPilote = nomPilote;
	this.adressePilote = adressePilote;
	this.salairePilote = salairePilote;
}
 
 
 
}
